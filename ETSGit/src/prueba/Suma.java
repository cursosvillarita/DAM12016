package prueba;
public class Suma {

	private int a;
	private int b;

	public Suma(int x, int y) {
		this.a = x;
		this.b = y;
	}

	public int bolimodif() {
		return a * b;
	}
	
	
	
	public int multi() {
		return a * b;
	}
	
	public int division() {
		return a / b;
	}

	
}
